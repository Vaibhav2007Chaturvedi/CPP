// Insertion Sort: Insertion Sort is a method of sorting where elements are picked one by one inserted into
//       their correct position in the sorted part of the array.
#include<iostream>
using namespace std;
int main(){
    int arr[5];
    int n=sizeof(arr)/sizeof(int);
    cout<<"Enter an array"<<endl;
    for(int i=0;i<n;i++){
        cin>>arr[i];
    }
    
} 